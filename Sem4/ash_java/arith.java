public class arith {
    public static void main(String args[])
    {
        int a=20,b=40;
        System.out.println("Values are a="+a+" b="+b);
        System.out.println("Sum:a+b = "+(a+b));
        System.out.println("Difference:b-a= "+(b-a));
        System.out.println("Product:a*b= "+(a*b));
        System.out.println("Division:b/a= "+(b/a));
        System.out.println("Modular:b%a= "+(b%a));
    }
}
